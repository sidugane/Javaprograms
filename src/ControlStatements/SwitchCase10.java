package ControlStatements;

public class SwitchCase10
{
public static void main(String[] args) 
{
	switch('E')
	{
	case 'B':
		System.out.println("We are in case:- B");
		break;
	case 'C':
		System.out.println("We are in case:- C");
		break;	
	case 'D':
		System.out.println("We are in case:- D");
		break;
	case 'E':
		System.out.println("We are in case:-E");
		break;
	case 'F':
		System.out.println("We are in case:-F");
		break;
	case 'G':
		System.out.println("We are in case:- G");
		break;
	case 'H':
		System.out.println("We are in case:-H");
		break;
		default:
			System.out.println("Alphabet not found...Try Again!");
	}	
}
}
