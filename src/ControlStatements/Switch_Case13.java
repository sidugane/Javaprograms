package ControlStatements;

public class Switch_Case13
{
public static void main(String[] args)
{
int a=1;

switch(a)
{
case 1:
	System.out.println("You are in the case 1");
break;
case 2:
	System.out.println("You are in the case 2");
break;
case 3:
	System.out.println("You are in the case 3");
break;
case 4:
	System.out.println("You are in the case 4");
break;
case 5:
	System.out.println("You are in the case 5");
break;
case 6:
	System.out.println("You are in the case 6");
break;
default:
	System.out.println("You are in default case");
}
}
}
