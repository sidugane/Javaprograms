package ControlStatements;

public class If_Statement12
{
public static void main(String[] args) 
{
	int num=14;
	if(num>1 & num<5)
	{
		System.out.println("You are > 5");
	}
	else if(num>6 & num<=25)
	{
		System.out.println("You are between 6 to 25");
	}
	else
	{
		System.out.println("You are less than 5");
	}
}
}
