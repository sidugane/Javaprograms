package Loops;

public class For_Loop2
{
/* Write a program to print numbers 15 to 25 using For Loop */
	
	public static void main(String[] args)
	{
	System.out.println("Printing Numbers from 15 to 25 using For Loop :");	
	for(int a=15;a<=25;a++)
	{
		System.out.println("Value of a is: "+a);
	}
	}
}
