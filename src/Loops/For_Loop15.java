package Loops;

public class For_Loop15 
{
public static void main(String[] args)

//Printing odd numbers from 1 to 10
{
	for(int a=1;a<=10;a=a+2)
	{
		System.out.println(a);
	}
	
	m1();
}

public static void m1()
{
	System.out.println("--------------");
	for(int i=50;i>=10;i=i-5)
	{
		System.out.println(i);
	}
}
}
