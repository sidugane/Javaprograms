package Loops;

public class For_Loop14 
{
	public static void main(String[] args) 
	{
		System.out.println("Printing nuumbers 1 to 10");
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("Printing nuumbers 10 to 1");
		
		for(int j=10;j>=1;j--)
		{
			System.out.println(j);
		}
	}
	
}
