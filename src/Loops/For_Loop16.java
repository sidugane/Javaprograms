package Loops;

public class For_Loop16
{
public static void main(String[] args)
{
	For_Loop16 s1=new For_Loop16();
    s1.m1();
System.out.println("................");

 m2();
	
}

public void m1()
{
	for(int i=1;i<=10;i++)
	{
		System.out.println("The Value of i is :"+i);
	}
}

public static void m2()
{
	for(int j=10;j>=1;j--)
	{
		System.out.println("The value of j is :"+j);
	}
}
}
